class WordsController < ApplicationController
  before_action :set_word, only: [:show, :edit, :update, :destroy, :vote]

  # GET /words
  # GET /words.json
  def index
    @words = Word.all
  end

  # GET /words/1
  # GET /words/1.json
  def show
  end

  # GET /words/new
  def new
    @word = Word.new
  end

  # GET /words/1/edit
  def edit
  end

  # POST /words
  # POST /words.json
  def create
    speaker = Person.find_by(name: params[:speaker])
    registrant = Person.find_by(name: params[:registrant])
    speaker_id = speaker.id unless speaker.nil?
    registrant_id = registrant.id unless registrant.nil?

    @word = Word.new(
      description: params[:description],
      speaker_id: speaker_id,
      registrant_id: registrant_id,
    )

    respond_to do |format|
      if @word.save
        format.html { redirect_to @word, notice: 'Word was successfully created.' }
        format.json { render :show, status: :created, location: @word }
      else
        format.html { render :new }
        format.json { render json: @word.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /words/1
  # PATCH/PUT /words/1.json
  def update
    respond_to do |format|
      if @word.update(word_params)
        format.html { redirect_to @word, notice: 'Word was successfully updated.' }
        format.json { render :show, status: :ok, location: @word }
      else
        format.html { render :edit }
        format.json { render json: @word.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /words/1
  # DELETE /words/1.json
  def destroy
    @word.destroy
    respond_to do |format|
      format.html { redirect_to words_url, notice: 'Word was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  def vote
    @word.update_attribute(:count, (@word.count + 1))
    flash[:success] = "Word was successfully updated."
    redirect_to :back
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_word
      @word = Word.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def word_params
      params.require(:word).permit(:description, :count, :speaker_id, :registrant_id)
    end
end
